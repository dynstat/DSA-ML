# Given an array of distinct integers candidates and a target integer target,
# return a list of all unique combinations of candidates where the chosen
# numbers sum to target. You may return the combinations in any order.
# 
# The same number may be chosen from candidates an unlimited number of times.
# Two combinations are unique if the frequency of at least one of the chosen
# numbers is different.
# 
# The test cases are generated such that the number of unique combinations that
# sum up to target is less than 150 combinations for the given input.
# 
# 
# Example 1:
# 
# 
# Input: candidates = [2,3,6,7], target = 7
# Output: [[2,2,3],[7]]
# Explanation:
# 2 and 3 are candidates, and 2 + 2 + 3 = 7. Note that 2 can be used multiple
# times.
# 7 is a candidate, and 7 = 7.
# These are the only two combinations.
# 
# 
# Example 2:
# 
# 
# Input: candidates = [2,3,5], target = 8
# Output: [[2,2,2,2],[2,3,3],[3,5]]
# 
# 
# Example 3:
# 
# 
# Input: candidates = [2], target = 1
# Output: []
# 
# 
# 
# Constraints:
# 
# 
# 1 <= candidates.length <= 30
# 2 <= candidates[i] <= 40
# All elements of candidates are distinct.
# 1 <= target <= 40
# 
# 
#

# @lc code=start
class Solution:
    def combinationSum(self, candidates: List[int], target: int) -> List[List[int]]:
        ans = []
        
        def rec(temp_list, i, total):
            # Base Condition 1: Found a valid combination
            if total == target:
                ans.append(temp_list.copy())
                return
            
            # Base Condition 2: Sum exceeds target (Missing in your original code)
            # This is critical if we allow reusing elements!
            if total > target:
                return

            # Base Condition 3: No more candidates to explore
            if i >= len(candidates):
                return

            # Include candidates[i]
            temp_list.append(candidates[i])
            # PASS 'i' INSTEAD OF 'i+1' to allow reusing the same element
            rec(temp_list, i, total + candidates[i])
            
            # Backtrack and skip candidates[i]
            temp_list.pop()
            rec(temp_list, i+1, total)

        rec([], 0, 0)
        return ans

